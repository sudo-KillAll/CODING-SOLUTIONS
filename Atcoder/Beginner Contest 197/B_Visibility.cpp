void solve(){
    ll n,m,x,y;cin>>n>>m>>x>>y;
    x--;y--;
    vector<string> v(n);
    for(ll i=0;i<n;i++)
        cin>>v[i];
    ll ans=0;
    // left
    for(ll j=y-1;j>=0;j--){
        if(v[x][j]=='#')
            break;
        ans++;
    }
    //right
    for(ll j=y+1;j<m;j++){
        if(v[x][j]=='#')
            break;
        ans++;
    }
    //bottom
    for(ll i=x-1;i>=0;i--){
        if(v[i][y]=='#')
            break;
        ans++;
    }
    //top
    for(ll i=x+1;i<n;i++){
        if(v[i][y]=='#')
            break;
        ans++;
    }
    if(v[x][y]=='.')
        ans++;
    cout<<ans<<"\n";

}
 
