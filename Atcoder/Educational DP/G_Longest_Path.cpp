const ll MAX = 100001;
vi dp(MAX,0);
bool visited[MAX]={false};

void dfs(ll node,map<ll,vi> &graph){
    visited[node]=true;
    vi v=graph[node];
    for(ll i=0;i<v.size();i++){
        if(!visited[v[i]])
            dfs(v[i],graph);
        dp[node]=max(dp[node],1+dp[v[i]]);
    }
}

