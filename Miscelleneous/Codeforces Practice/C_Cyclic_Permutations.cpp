const ll mod = 1e9+7;

ll facMod(ll n){
    ll ans = 1;
    for(ll i=1;i<=n;i++){
        ans = (ans*i)%mod;
    }
    return ans;
}

ll power(ll x, unsigned ll y, ll p)
{
    ll res = 1;     // Initialize result
 
    x = x % p; // Update x if it is more than or
                // equal to p
  
    if (x == 0) return 0; // In case x is divisible by p;
 
    while (y > 0)
    {
        // If y is odd, multiply x with result
        if (y & 1)
            res = (res*x) % p;
 
        // y must be even now
        y = y>>1; // y = y/2
        x = (x*x) % p;
    }
    return res;
}

void solve(){
    ll n;cin>>n;
    ll ans = facMod(n)-power(2,n-1,mod);
    if(ans<0)
        ans+=mod;
    cout<<ans;
}
 
