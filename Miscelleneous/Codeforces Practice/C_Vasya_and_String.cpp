ll helper(string s,ll n,ll k,char x){
    ll lft = 0, rt = 0,cnt = 0,ans = 0;
    while(rt<n){
        if(s[rt]!=x) cnt++;
        while(cnt>k){
            if(s[lft]!=x)   --cnt;
            lft++;
        }
        ans = max(ans,rt-lft+1);
        rt++;
    }
    return ans;
}

