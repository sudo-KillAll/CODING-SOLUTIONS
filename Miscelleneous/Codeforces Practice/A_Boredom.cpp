void solve(){
    ll n;cin>>n;
    map<ll,ll> mp;
    for(ll i=0;i<n;i++){
        ll x;cin>>x;
        mp[x]++;
    }
    vi v;
    for(auto it=mp.begin();it!=mp.end();it++)   v.pb(it->F);

    if(v.size()==1){
        cout<<v[0]*mp[v[0]];
        return;
    }
    vi dp(v.size());
    dp[0] = mp[v[0]]*v[0];
    v[1]==v[0]+1?dp[1] = max(dp[0],mp[v[1]]*v[1]):dp[1] = dp[0]+mp[v[1]]*v[1];
    for(ll i=2;i<v.size();i++){
        if(v[i-1]==v[i]-1){
            dp[i] = max(dp[i-2]+mp[v[i]]*v[i],dp[i-1]);
        }
        else{
            dp[i] = dp[i-1]+mp[v[i]]*v[i];
        }
    }
    cout<<dp[v.size()-1];
}
 
