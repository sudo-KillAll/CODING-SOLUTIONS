void solve(){
    ll n;cin>>n;
    vi b(n+2);
    for(auto &i:b)  cin>>i;
    
    sort(b.begin(),b.end());
    ll s = 0;
    for(ll i=0;i<n;i++)
        s+=b[i];
    if(s==b[n] || s==b[n+1]){
        for(ll i=0;i<n;i++)
            cout<<b[i]<<" ";
        cout<<"\n";
    }
    else{
        ll ind = -1;
        for(ll j=n-1;j>=0;j--){
            ll tmp = s;

            tmp-=b[j];
            tmp+=b[n];
            if(tmp==b[n+1]){
                ind = j;
                break;
            }
        }
        if(ind==-1){
            cout<<"-1\n";
            return;
        }
        for(ll i=0;i<=n;i++){
            if(i!=ind){
                cout<<b[i]<<" ";
            }
        }
        cout<<"\n";
    }
}
 
