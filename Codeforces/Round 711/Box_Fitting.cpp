void solve(){
    ll n,w;cin>>n>>w;
    vi v;input(v,n);
    sort(v.begin(),v.end(),greater<int>());
    map<ll,ll> mp;
    for(auto i:v)
        mp[i]++;
    ll ans=0;
    
    for(auto it=mp.rbegin();it!=mp.rend();it++){
        while(it->S>0){
            ans++;
            ll val = it->F,maxi = w;
            while(val>=1){
                if(val<=maxi){
                    if(mp[val]>0){
                        mp[val]--;
                        maxi-=val;
                    }
                    else{
                        val/=2;
                    }
                }
                else{
                    val/=2;
                }
            }
        }
        
    }
    cout<<ans<<"\n";
}
 
