void solve(){

    ll n;cin>>n;
    vector<pair<ll,ll>> v;
    for(ll i=0;i<n;i++){
        ll a,b;cin>>a>>b;
        pair<ll,ll> p(a,b);
        v.pb(p);
    }
    vi tm;input(tm,n);
    ll time=0;
    for(ll i=0;i<n-1;i++){
        if(i!=0)
            time+=v[i].F-v[i-1].S+tm[i];
        else
            time+=v[i].F+tm[i];
        ll departure = max(v[i].S,time+(v[i].S-v[i].F+1)/2);
        time=(departure);
        //cout<<time<<" ";

    }
    if(n>1)
        time+=v[n-1].F-v[n-2].S+tm[n-1];
    else
        time+=v[0].F+tm[0];
    cout<<time<<"\n";

}
 
