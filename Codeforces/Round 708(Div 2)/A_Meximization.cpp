void solve(){

    ll n;cin>>n;
    vi v;
    
    map<ll,ll> mp;
    for(ll i=0;i<n;i++){
        ll x;cin>>x;
        v.pb(x);
        mp[x]++;
    }
    sort(v.begin(),v.end());
    ll mex;
    for(ll i=0;i<101;i++){
        if(!binary_search(v.begin(),v.end(),i)){
            mex=i;
            break;
        }
    }
    vi ans;
    for(auto it=mp.begin();it!=mp.end();it++){
        if(it->F<=mex){
            ans.pb(it->F);
            it->S--;
        }
        else{
            break;
        }
    }
    for(auto it=mp.begin();it!=mp.end();it++){
        if(it->S>0){
            for(ll i=0;i<it->S;i++){
                ans.pb(it->F);
            }
        }
    }
    print(ans);
    cout<<"\n";

}
 
